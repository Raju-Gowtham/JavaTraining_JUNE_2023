// java program for festching first and last indexes of string
public class Day3CharAtExample3{
  public static void main(String[] args)
  {
    String str="Welcome to java Training";
	int strLength=str.length();
	//Fetching first character
	System.out.println("Character at index 0 is:"+str.charAt(0));
	//Fetching last Character
	System.out.println("Character at last index is:"+str.charAt(strLength-1));
	}
}
	
	