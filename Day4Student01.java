// java program to illustrate how to define a class and fields
class Student{
     int id;
	 String name;
}
class Day4Student01{
	 public static void main(String[] args)
	 {
	   Student s1=new Student();
	   System.out.println(s1.id);
	   System.out.println(s1.name);
	  }// main()
}//class